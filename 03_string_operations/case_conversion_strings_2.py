the_string = "Python Scripting for DevOps"
the_string_lower = the_string.lower()
the_string_upper = the_string.upper()
the_string_casefold = the_string.casefold()
the_string_swapcase = the_string.swapcase()
the_string_title = the_string.title()
the_string_capitalize = the_string.capitalize()
print(the_string_lower)         #The lower() method returns a string where all characters are lower case.
print(the_string_upper)         #The upper() method returns a string where all characters are in upper case.
print(the_string_casefold)      #The casefold() method returns a string where all the characters are lower case.
print(the_string_swapcase)      #The swapcase() method returns a string where all the upper case letters are lower case and vice versa.
print(the_string_title)         #The title() method returns a string where the first character in every word is upper case.
print(the_string_capitalize)    #The capitalize() method returns a string where the first character is upper case, and the rest is lower case
print(the_string)

